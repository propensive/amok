package amok

import soundness.{Node as _, *}

object Modifier:
  val all: List[Modifier] = values.to(List)

enum Modifier:
  case `case`, `private`, `abstract`, `open`, `final`, `erased`, `transparent`, `inline`, `lazy`,
       `sealed`, `override`, `opaque`, `infix`, `into`, `tracked`, `protected`

  def keyword: Syntax = Syntax.Symbolic(this.toString.tt.lower)

export Modifier.*
